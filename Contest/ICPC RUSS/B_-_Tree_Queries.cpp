const int MAX_N =(int)2e5+7;
const int LOG = 20;
vector<int> children[MAX_N];
int up[MAX_N][LOG] ; // up[v][j] is 2^j-th ancestor of v
int depth[MAX_N];
int vis[MAX_N];
void dfs(int a) {
    vis[a]=1;
	for(int b : children[a]) {
        if(vis[b]) continue;
		depth[b] = depth[a] + 1;
		up[b][0] = a; // a is parent of b
		for(int j = 1; j < LOG; j++) {
			up[b][j] = up[up[b][j-1]][j-1];
		}
		dfs(b);
	}
}
 
int lca(int a, int b ) { // O(log(N))
	if(depth[a] < depth[b]) {
		swap(a, b);
	}
	// 1) Get same depth.
	int k = depth[a] - depth[b];
	for(int j = LOG - 1; j >= 0; j--) {
		if(k & (1 << j)) {
			a = up[a][j]; // parent of a
		}
	}
	// 2) if b was ancestor of a then now a==b
	if(a == b) {
		return a;
	}
	// 3) move both a and b with powers of two
	for(int j = LOG - 1; j >= 0; j--) {
		if(up[a][j] != up[b][j]) {
			a = up[a][j];
			b = up[b][j];
		}
	}
	return up[a][0];
}
bool cmp(int a , int b){
    return (depth[a]>=depth[b]);
}
