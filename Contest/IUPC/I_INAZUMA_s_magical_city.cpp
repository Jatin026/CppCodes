void solve(){
    int n , m ;
    cin >> n >> m;
    vll value(n);
    for(auto &x : value){
        cin >> x;
    }
    vi vis(n,0),par(n,0);
    vvi adj(n);
    for (int i = 0; i < m; i++)
    {
        int a, b;
        cin >> a>> b;
        --a;--b;
        adj[a].pb(b);
    }
    dsu d(n+2);
    int T = 1;
    for (int i = 0; i < n; i++)
    {
        if(!vis[i]){
            vi nodes;
            bool flag = false;
            queue<int> q;
            q.push(i);
            par[i] = -1;
            vis[i] = T;
            vi passed;
            pr info = {-1,-1};
            while(!q.empty()){
                int v = q.front();
                passed.pb(v);
                q.pop();
                for(auto u : adj[v]){
                    if(vis[u] == T){
                        flag = 1;
                        passed.pb(v);
                        info = {u,v};
                        break;
                    }
                    q.push(u);
                    par[u] = v;
                    vis[u] = T;
                }
            }
            if(flag){
                
                for (int j = info.ss; (j != info.ff || j == -1); j = par[j])
                {
                    if(j!=-1) nodes.pb(j);    
                }
                if(info.ff!=-1) nodes.pb(info.ff);
                for (int j = 0; j < nodes.size() - 1; j++)
                {
                    if(nodes[j] < n && nodes[j+1] < n)d.merge(nodes[j],nodes[j+1]);
                }
                for(auto x : nodes) {
                    par[x] = -1;
                    vis[x] = 0;
                }
            }

            T++;
        }
    }
    ll ans = 0;
    vvi group = d.groups();
    for(auto x : group){
        ll val = 1e17;
        for(auto y : x){
            if(y<n) val = min(val ,  value[y]);
        }
        if(val<1e10) ans+=val;
    }
    cout << ans ;
}
