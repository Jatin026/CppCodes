ll BinExpItr(ll a , ll b){
    ll res=1;
    while(b){
        if(b&1){
            res=(res*a)%mod;
        }
        a=(a*a)%mod;
        b>>=1;
    }
    return res;
}


void solve(){
    int n;cin>>n;
    vi v(n); for(auto &x : v) cin>>x;
    map<int,int> odd , eve;
    for (int i = 0; i < n; i++)
    {
        if(i&1){
            eve[v[i]]++;
        }
        else{
            odd[v[i]]++;
        }
    }
    int lenO=(n+1)/2, lenE=(n/2);
    vpr ve,vo;
    for(auto x : eve){
        ve.pb({x.second,x.first});
    }
    for(auto x : odd){
        vo.pb({x.second,x.first});
    }
    sort(all(ve));sort(all(vo));
    map<int,int> o2,e2;
    for (int i = 0; i < n; i++)
    {
        if(i&1){
            if(i+2<n && v[i]==v[i+2]) e2[v[i]]++;
        }
        else{
            if(i+2<n && v[i]==v[i+2]) o2[v[i]]++;
        }
    }
    
    
    int ans=lenO+lenE;
    for (int i = 0; i < ve.size(); i++)
    {
        for (int j = 0; j < vo.size(); j++)
        {
            if(ve[i].second!=vo[j].second && !(e2.count(vo[i].second)==1 && o2.count(ve[j].second)==1)){
                ans=min(ans,lenO-vo[j].first + lenE - ve[i].first);
            }
        }
        
    }
    

    cout<<ans<<nline;
}
