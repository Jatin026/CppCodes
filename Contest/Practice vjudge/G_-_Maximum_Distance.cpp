void dfs(int v , vpr &par , vi &vis){
    vis[v] = 1;
    for(auto x : adj[v]){
        int u = x.first , w = x.second;
        if(vis[u]) continue;
        par[u] = {v,w};
        dfs(u,par,vis);
    }
} 

