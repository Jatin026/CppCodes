range intersection(long double a , long double b, long double c , long double d){
    range obj;
    obj.b=min(max(a,b),max(c,d));
    obj.a=max(min(a,b),min(c,d));
    return obj;
}
bool check(long double val,vector<int> &v,vector<int> &t){
    range obj;
    for (int i = 0; i < v.size(); i++)
    {
        if(i==0) obj=intersection(max(v[i]+t[i]-val,(lld)0),max(val-t[i]+v[i],(lld)0),max(v[i+1]+t[i+1]-val,(lld)0),max(val-t[i+1]+v[i+1],(lld)(0)));
        else obj=intersection(obj.a,obj.b,max(v[i]+t[i]-val,(lld)0),max(val-t[i]+v[i],(lld)0));
        if(obj.a > obj.b) return 0;
    }
    return 1;
}
